CREATE TABLE IF NOT EXISTS `Orders` (
  OrderID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  tavernID INT,
  totalCost FLOAT,
  deliveryDate VARCHAR(8),
  CONSTRAINT `fk_orders_tavern`
    FOREIGN KEY (tavernID) REFERENCES Taverns (`TavernID`)
    ON DELETE RESTRICT
    ON UPDATE RESTRICT
);

